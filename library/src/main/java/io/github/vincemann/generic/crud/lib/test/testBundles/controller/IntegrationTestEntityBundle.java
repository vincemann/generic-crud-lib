package io.github.vincemann.generic.crud.lib.test.testBundles.controller;

import io.github.vincemann.generic.crud.lib.model.IdentifiableEntity;
import io.github.vincemann.generic.crud.lib.test.testBundles.abs.callback.TestCallback;
import io.github.vincemann.generic.crud.lib.test.testBundles.controller.update.updateIteration.abs.UpdateTestEntityBundleIteration;
import io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.TestRequestEntityModification;
import io.github.vincemann.generic.crud.lib.test.testBundles.abs.CallbackableTestEntityBundle;
import lombok.Getter;
import org.springframework.lang.Nullable;

@Getter
public abstract class IntegrationTestEntityBundle<E extends IdentifiableEntity, PreTestCallbackE, PostTestCallbackE>
        extends CallbackableTestEntityBundle<E, PreTestCallbackE, PostTestCallbackE> {

    /**
     * Set this, if you dont want the default {@link io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.TestRequestEntity} that will be generated by the {@link io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.factory.TestRequestEntityFactory}
     * but instead, you want to edit it, you can with {@link TestRequestEntityModification} for this specific tested Entity for this specfic test.
     * <p>
     * For Update Tests see {@link UpdateTestEntityBundleIteration}.
     */
    @Nullable
    private TestRequestEntityModification testRequestEntityModification;

    public IntegrationTestEntityBundle(TestCallback<PreTestCallbackE> preTestCallback, TestCallback<PostTestCallbackE> postTestCallback, E entity, @Nullable TestRequestEntityModification testRequestEntityModification) {
        super(preTestCallback, postTestCallback, entity);
        this.testRequestEntityModification = testRequestEntityModification;
    }

    public IntegrationTestEntityBundle(E entity) {
        super(entity);
    }

}
