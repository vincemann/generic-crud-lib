package io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testBundles;

import io.github.vincemann.generic.crud.lib.model.IdentifiableEntity;
import io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.TestRequestEntityModification;
import lombok.Builder;
import lombok.Getter;
import org.junit.jupiter.api.Assertions;
import org.springframework.lang.Nullable;

@Getter
public abstract class TestEntityBundle<E extends IdentifiableEntity> {
    private E entity;


    @Nullable
    /**
     * Set this, if you dont want the default {@link io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.TestRequestEntity} that will be generated by the {@link io.github.vincemann.generic.crud.lib.test.controller.springAdapter.testRequestEntity.factory.TestRequestEntityFactory}
     * but instead, you want to edit it, you can with {@link TestRequestEntityModification} for this specific tested Entity for this specfic test.
     *
     * For Update Tests see {@link UpdateTestEntityBundle}.
     */
    private TestRequestEntityModification findTestRequestEntityModification;
    @Nullable
    private TestRequestEntityModification deleteTestRequestEntityModification;
    @Nullable
    private TestRequestEntityModification createTestRequestEntityModification;


    @Builder
    public TestEntityBundle(E entity, @Nullable TestRequestEntityModification findTestRequestEntityModification, @Nullable TestRequestEntityModification deleteTestRequestEntityModification, @Nullable TestRequestEntityModification createTestRequestEntityModification) {
        this.entity = entity;
        this.findTestRequestEntityModification = findTestRequestEntityModification;
        this.deleteTestRequestEntityModification = deleteTestRequestEntityModification;
        this.createTestRequestEntityModification = createTestRequestEntityModification;
    }

    public TestEntityBundle(E entity) {
        this.entity = entity;
    }

    protected void verifyBundle(){
        Assertions.assertNotNull(entity);
    }
}
